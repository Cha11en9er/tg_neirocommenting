"""Промпты для двух запросов к OpenRouter: классификация поста и генерация комментария."""

from neuro_config import (
    COMMENT_STYLE_DO,
    COMMENT_STYLE_DONT,
    SUITABLE_POST_TYPES,
    UNSUITABLE_POST_TYPES,
)

# Примеры из ТЗ — few-shot для генерации (без образа аккаунта)
COMMENT_EXAMPLES = """
Пример (разбор сделки):
Пост: автор делится закрытой плюсовой сделкой по BTC
Комментарий: Тоже сегодня зафиксировал по BTC — зашёл на пробое уровня 94к, вышел частями. Первую половину на 97к, остаток держу. Хорошо когда план заранее прописан, иначе начинаешь суетиться на движении.

Пример (убыточная сделка):
Пост: автор показывает убыточную сделку или безубыток
Комментарий: Узнаю ситуацию. У меня на прошлой неделе похожее — вышел в безубыток, а потом цена пошла куда надо без меня. Стараюсь теперь делить позицию: часть фиксирую по плану, часть держу. Психологически легче.

Пример (ИИ в трейдинге):
Пост: использование нейросетей в анализе рынка
Комментарий: Пользуюсь уже несколько месяцев для разбора альтов — реально ускоряет. Раньше на анализ 5 монет уходил час-полтора, сейчас минут 20-25. Главное правильно сформулировать запрос, иначе получаешь общие слова.

Пример (обзор BTC):
Пост: еженедельный технический разбор биткоина
Комментарий: Смотрю на ту же зону 91-93к как на ключевую. Если удержим — думаю ещё одна попытка на хай. Если пробьём вниз с объёмом — пойдём тестировать 86к. Пока держу позицию с тайт стопом, не тороплюсь с выводами.

Пример (проп-трейдинг):
Пост: прохождение челленджа на проп-счёте
Комментарий: Прошёл челлендж со второй попытки. Первый раз слил на нарушении дневного лимита просадки — не уследил. Второй раз поставил будильник на допустимый минус дня. Главное не жадничать на этапе проверки, цель — пройти, а не заработать.
""".strip()


def _bullet_list(items: list[str]) -> str:
    return "\n".join(f"• {item}" for item in items)


def build_classify_system_prompt() -> str:
    return f"""Ты — фильтр постов Telegram-канала о криптотрейдинге.
Определи, стоит ли оставлять комментарий под этим постом.

Подходящие типы постов:
{_bullet_list(SUITABLE_POST_TYPES)}

Неподходящие типы — отклоняй:
{_bullet_list(UNSUITABLE_POST_TYPES)}

Ответь ТОЛЬКО валидным JSON без markdown:
{{"suitable": true или false, "post_type": "краткий тип поста", "reason": "одно предложение"}}"""


def build_comment_system_prompt() -> str:
    return f"""Ты пишешь комментарии под постами криптотрейдинговых Telegram-каналов.
Комментарий — от первого лица, как живой участник рынка. Без прямой рекламы.

Делать:
{_bullet_list(COMMENT_STYLE_DO)}

Не делать:
{_bullet_list(COMMENT_STYLE_DONT)}

{COMMENT_EXAMPLES}

Верни только текст комментария, без кавычек и пояснений."""
